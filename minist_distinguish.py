from google.colab import files
from PIL import Image
import numpy as np
import matplotlib.pyplot as plt

print("📤 Upload your handwritten digit image (28x28px grayscale recommended)")
print("Supported formats: PNG, JPG, JPEG,jfif")

# Upload image
uploaded = files.upload()

# Process first uploaded image
filename = list(uploaded.keys())[0]
print(f"Processing: {filename}")

# Load and preprocess image
img = Image.open(filename).convert('L')  # Convert to grayscale
img = img.resize((28, 28))  # Resize to 28x28
img_array = np.array(img) / 255.0  # Normalize
img_array = img_array.reshape(1, 28, 28, 1)  # Reshape for model

# Predict
prediction = model.predict(img_array)
predicted_digit = np.argmax(prediction)
confidence = np.max(prediction) * 100

# Create comprehensive visualization
fig = plt.figure(figsize=(16, 10))

# 1. Original vs Processed Image
plt.subplot(2, 3, 1)
plt.imshow(img, cmap='gray')
plt.title('Original Image', fontsize=14, fontweight='bold')
plt.axis('off')

plt.subplot(2, 3, 2)
plt.imshow(img_array.squeeze(), cmap='gray')
plt.title(f'Processed (28x28)\nPredicted: {predicted_digit}',
          fontsize=14, fontweight='bold', color='green' if confidence > 90 else 'orange')
plt.axis('off')

# 2. Confidence Bar Chart (highlighted winner)
plt.subplot(2, 3, 3)
colors = ['lightgray'] * 10
colors[predicted_digit] = 'gold' if confidence > 90 else 'orange'
bars = plt.bar(range(10), prediction[0]*100, color=colors, alpha=0.8, edgecolor='black')
plt.title('Confidence Scores (%)', fontsize=14, fontweight='bold')
plt.xlabel('Digit')
plt.ylabel('Confidence %')
plt.xticks(range(10))
plt.ylim(0, 100)

# Highlight max bar
bars[predicted_digit].set_alpha(1.0)
bars[predicted_digit].set_color('darkgreen' if confidence > 90 else 'darkorange')

# Add confidence text
plt.text(predicted_digit, confidence + 1, f'{confidence:.1f}%',
         ha='center', va='bottom', fontweight='bold', fontsize=12, color='white')

# 3. Top 5 Probabilities Horizontal Bar
plt.subplot(2, 3, (4, 6))
top5_indices = np.argsort(prediction[0])[::-1][:5]
top5_probs = prediction[0][top5_indices] * 100

colors_top5 = ['gold' if i == predicted_digit else 'silver' for i in top5_indices]
bars_top5 = plt.barh(range(5), top5_probs, color=colors_top5, alpha=0.9, edgecolor='black')
plt.title('Top 5 Predictions', fontsize=14, fontweight='bold')
plt.xlabel('Confidence %')
plt.yticks(range(5), [f'Digit {top5_indices[i]}' for i in range(5)])
plt.xlim(0, 100)

# Add percentage labels
for i, (bar, prob) in enumerate(zip(bars_top5, top5_probs)):
    plt.text(prob + 1, i, f'{prob:.1f}%', va='center', fontweight='bold')

plt.tight_layout()
plt.show()

# Print detailed results
print(f"\n🎯 FINAL PREDICTION: {predicted_digit}")
print(f"📊 CONFIDENCE: {confidence:.1f}%")
print(f"\n📈 ALL PROBABILITIES:")
for i in range(10):
    print(f"  Digit {i}: {prediction[0][i]*100:.1f}%")
print(f"\n🏆 Top 5: {dict(zip(top5_indices, np.round(top5_probs, 1)))}")